package ch02;

public class CharExample {
    public static void main(String[] args) {
        char c1 = 97;
        int c2 = '가';
        int c3 = 'Z';
        char c4 = 55203;

        System.out.println("c1 = " + c1); // c1 = a
        System.out.println("c2 = " + c2); // c2 = 44032
        System.out.println("c3 = " + c3); // c3 = 90
        System.out.println("c4 = " + c4); // c4 = 힣
    }
}
